operator = input("Enter a operatot (+ - * /)")
num1 = float(input("Enter number: "))
num2 = float(input("Enter number: "))
    
if operator == "+":
    result = num1 + num2
    print(result)
elif operator == "-":
    result = num1 - num2
    print(result)
elif operator == "*":
    result = num1 * num2
    print(result)
elif operator == "/":
    result = num1 / num2
    print(result)
    
    
